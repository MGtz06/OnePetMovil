  Future<void> avisarEventosPropietario(
  BuildContext context, 
  List<MascotaRow> todasLasMascotas,
  List<CitaRow> todasLasCitas
) async {
  final hoy = DateTime.now();
  List<Widget> widgetsCitas = [];
  List<Widget> widgetsCumples = [];

  // 1. LÓGICA DE CITAS (Igual a la anterior)
  for (var cita in todasLasCitas) {
    final mascotaMatch = todasLasMascotas.cast<MascotaRow?>().firstWhere(
      (m) => m?.folio.trim() == cita.mascota.trim(),
      orElse: () => null,
    );

    if (mascotaMatch != null) {
      String horaRaw = cita.hora.toString();
      String horaLimpia = horaRaw.contains(':') ? horaRaw.split(':').take(2).join(':') : '--:--';

      widgetsCitas.add(
        _buildRowEvento(Icons.calendar_today, mascotaMatch.nombre, horaLimpia, const Color(0xFFE0F2F1), const Color(0xFF00796B)),
      );
    }
  }

  // 2. LÓGICA DE CUMPLEAÑOS
  for (var m in todasLasMascotas) {
    DateTime fNac = m.fechanacimiento;
    if (fNac.day == hoy.day && fNac.month == hoy.month) {
      int edad = hoy.year - fNac.year;
      String textoEdad = edad > 0 ? '$edad años' : '1er año';
      widgetsCumples.add(
        _buildRowEvento(Icons.cake, m.nombre, textoEdad, const Color(0xFFFCE4EC), const Color(0xFFC2185B)),
      );
    }
    }

  // 3. MOSTRAR DIÁLOGO SI HAY ALGO
  if (widgetsCitas.isNotEmpty || widgetsCumples.isNotEmpty) {
    await showDialog(
      context: context,
      barrierDismissible: false,
      builder: (BuildContext context) {
        return AlertDialog(
          shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(20.0)),
          title: const Column(
            children: [
              Icon(Icons.notifications_active, size: 40, color: Color(0xFF455A64)),
              SizedBox(height: 10),
              Text("Recordatorios de Hoy", textAlign: TextAlign.center),
              Divider(),
            ],
          ),
          content: SizedBox(
            width: double.maxFinite,
            child: SingleChildScrollView(
              child: Column(
                mainAxisSize: MainAxisSize.min,
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  if (widgetsCitas.isNotEmpty) ...[
                    const Text("CITAS:", style: TextStyle(fontWeight: FontWeight.bold, color: Colors.grey)),
                    ...widgetsCitas,
                    const SizedBox(height: 10),
                  ],
                  if (widgetsCumples.isNotEmpty) ...[
                    const Text("CUMPLEAÑOS:", style: TextStyle(fontWeight: FontWeight.bold, color: Colors.grey)),
                    ...widgetsCumples,
                  ],
                ],
              ),
            ),
          ),
          actions: [
            TextButton(
              child: const Text("CERRAR", style: TextStyle(color: Color(0xFF455A64), fontWeight: FontWeight.bold)),
              onPressed: () => Navigator.of(context).pop(),
            ),
          ],
        );
      },
    );

    // MARCAR COMO MOSTRADO
    FFAppState().marcarAgendaComoMostrada();
  }
}

