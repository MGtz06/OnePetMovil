@override
void initState() {
  super.initState();
  _model = createModel(context, () => InicioPropietarioModel());

  SchedulerBinding.instance.addPostFrameCallback((_) async {
    // Obtenemos la fecha de hoy en formato YYYY-MM-DD
    final hoyString = DateTime.now().toIso8601String().split('T')[0];

    // 1. Buscamos las mascotas del propietario (necesario para mascotasU)
    final mascotasRes = await MascotaTable().queryRows(
      queryFn: (q) => q.eqOrNull(
        'propietario',
        FFAppState().folioUsuario,
      ),
    );

    // 2. Buscamos las citas de hoy que estén pendientes
    final citasRes = await CitaTable().queryRows(
      queryFn: (q) => q
          .eqOrNull('propietario', FFAppState().folioUsuario)
          .eqOrNull('fecha', hoyString)
          .eqOrNull('estado', 'PEND')
          .order('fecha',
              ascending: true)
          .order('hora',
              ascending: true),
    );

    // 3. Llenamos el modelo con los resultados
    _model.mascotasU = mascotasRes;
    _model.citasNuevas = citasRes;
    
    _model.mascotasList = mascotasRes
        .map((e) => valueOrDefault<String>(e.folio, 'n'))
        .toList()
        .cast<String>();

    // Refrescamos la UI para que mascotasU esté disponible de inmediato
    safeSetState(() {});

    // 4. Lógica de Notificación
    // Solo ejecutamos la función si hay algo que avisar Y el App State es falso
    if ((mascotasRes.isNotEmpty || citasRes.isNotEmpty) && 
        !FFAppState().agendaMostradaHoy) { 
      
      // Llamamos a la función. 
      // IMPORTANTE: Asegúrate de que la función sea Future<void> para no tener errores.
      await avisarEventosPropietario(context, mascotasRes, citasRes);
    }
  });
}


